def markdown_to_docx(md_text: str, out_path: Path, source_dir: Path) -> None:
    doc = Document()
    set_styles(doc)
    section = doc.sections[0]
    section.top_margin = Inches(0.75)
    section.bottom_margin = Inches(0.7)
    section.left_margin = Inches(0.8)
    section.right_margin = Inches(0.8)
    add_page_num(section)

    lines = md_text.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i].rstrip()
        if not line.strip():
            i += 1
            continue
        if line.startswith("#"):
            level = len(line) - len(line.lstrip("#"))
            text = line[level:].strip()
            doc.add_heading(text, level=min(level, 3))
            i += 1
            continue
        if line.startswith("!["):
            m = re.match(r"!\[(.*?)\]\((.*?)\)", line)
            if m:
                rel = m.group(2)
                path = (source_dir / rel).resolve()
                if path.exists():
                    p = doc.add_paragraph()
                    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
                    p.add_run().add_picture(str(path), width=Inches(5.9))
                    cap = doc.add_paragraph()
                    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
                    cap.add_run(m.group(1)).italic = True
            i += 1
            continue
        if line.startswith("|"):
            table_lines = [line]
            j = i + 1
            while j < len(lines) and lines[j].startswith("|"):
                table_lines.append(lines[j].rstrip())
                j += 1
            if len(table_lines) >= 2:
                rows = []
                for idx, tline in enumerate(table_lines):
                    cells = [c.strip() for c in tline.strip("|").split("|")]
                    if idx == 1 and all(set(c) <= {"-", ":"} for c in cells):
                        continue
                    rows.append(cells)
                if rows:
                    table = doc.add_table(rows=1, cols=len(rows[0]))
                    table.style = "Table Grid"
                    hdr = table.rows[0].cells
                    for idx, text in enumerate(rows[0]):
                        hdr[idx].text = text
                        set_cell_shading(hdr[idx], "D9EAF7")
                        for p in hdr[idx].paragraphs:
                            for r in p.runs:
                                r.bold = True
                    for row in rows[1:]:
                        cells = table.add_row().cells
                        for idx, text in enumerate(row):
                            cells[idx].text = text
                i = j
                continue
        if line.startswith("- "):
            items = []
            while i < len(lines) and lines[i].startswith("- "):
                items.append(lines[i][2:].strip())
                i += 1
            add_bullets(doc, items)
            continue
        if re.match(r"\d+\.\s+", line):
            items = []
            while i < len(lines) and re.match(r"\d+\.\s+", lines[i]):
                items.append(re.sub(r"^\d+\.\s+", "", lines[i]).strip())
                i += 1
            for item in items:
                doc.add_paragraph(item, style="List Number")
            continue
        p = doc.add_paragraph()
        p.add_run(line.strip())
        i += 1

    doc.save(out_path)


